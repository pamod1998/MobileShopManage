    private void btnSaveSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveSupplierActionPerformed

		// TODO add your handling code here:
		String id = txtSupplierID.getText();
		String name = txtSupplierName.getText();
		String nic = txtSupplierNIC.getText();
		String address = txtSupplierAddress.getText();
		String email = txtSupplierEmail.getText();
		int tp = Integer.parseInt(txtSupplierTp.getText());

		Supplier s1 = new Supplier(id, name, nic, address, email, tp);

		try {

			if (new SupplierFormController().saveSupplier(s1)) {
				JOptionPane.showMessageDialog(this, "Saved");
				DefaultTableModel dtm = (DefaultTableModel) SupplierTable.getModel();
				Object[] rowData = {
					s1.getSupplier_id(),
					s1.getSupplier_Name(),
					s1.getSupplier_NIC(),
					s1.getSupplier_Address(),
					s1.getSupplier_Email(),
					s1.getSupplier_Mobile_Number()
				};
				dtm.addRow(rowData);
				txtSupplierID.setText(null);
				txtSupplierName.setText(null);
				txtSupplierNIC.setText(null);
				txtSupplierAddress.setText(null);
				txtSupplierEmail.setText(null);
				txtSupplierTp.setText(null);
				getAll();
			} else {
				JOptionPane.showMessageDialog(this, "Try Again");
			}

		} catch (ClassNotFoundException | SQLException ex) {
			Logger.getLogger(SupplierForm.class.getName()).log(Level.SEVERE, null, ex);
		}

    }//GEN-LAST:event_btnSaveSupplierActionPerformed

