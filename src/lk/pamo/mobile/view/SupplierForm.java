	private void getAll() {
		try {
			ArrayList<Supplier> allsuppliers = SupplierFormController.getAllSuppliers();
			DefaultTableModel dtm = (DefaultTableModel) SupplierTable.getModel();
			dtm.setRowCount(0);
			for (Supplier supplier : allsuppliers) {
				Object[] rowData = {supplier.getSupplier_id(), supplier.getSupplier_Name(), supplier.getSupplier_NIC(), supplier.getSupplier_Address(), supplier.getSupplier_Email(), supplier.getSupplier_Mobile_Number()};
				dtm.addRow(rowData);
			}
		} catch (ClassNotFoundException | SQLException ex) {
			Logger.getLogger(SupplierForm.class.getName()).log(Level.SEVERE, null, ex);
		}
	}






